'''
faça um programa que pergunte quantas horas você trabalhou no mês e valor pago por hora
Calcule o total do salário
'''

slry = float(input('Valor da hora trabalhada: R$ '))
h_mnth = float(input('Total de horas trabalhadas no mês: '))
print(f'Foram trabalhadas {h_mnth} horas e o salário é de R${slry * h_mnth:.2f}')

